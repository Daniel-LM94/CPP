#include <iostream>
using namespace std;

int SUMAR (int a, int b) 
{ 
    return a + b; 
}
  
int main(int argc, char const *argv[])
{
    system ("cls");
    cout << SUMAR(5, 2) << endl;
    cout << endl;   
    cout << SUMAR(15, 2) << endl;
    cout << endl;   
    cout << SUMAR(5, 71) << endl;
    cout << endl;   
    cout << SUMAR(9, 8) << endl;
    cout << endl;     
        
    return 0;
}
